        </div>
        {#if qrImage}
          <div class="qr-image">
            <img alt="QR" src="{qrImage}" on:load="set({ loading: false })" />
          </div>
        {/if}
     {/if}
  {:elseif view === 'error'}
    <div class="error mchild">
      <div class="error-text">{error}</div>
      <br />
      <div class="btn" on:click="init()">Retry</div>
