<script>
  import { RazorpayConfig } from 'common/Razorpay';
  import { processInstrument } from 'checkoutframe/personalization';
  import DowntimesStore from 'checkoutstore/downtimes.js';

  export default {
    components: {
      Callout: 'templates/views/ui/Callout.svelte',
      AsyncLoading: 'templates/views/ui/AsyncLoading.svelte',
      FeeBearer: 'templates/views/feebearer.svelte',
    },

    oncreate() {
      this.init();
    },

    data() {
      return {
        RazorpayConfig,
        loading: false,
        qrImage: null,
        error: null,
        view: 'qr',
        down: false,
      }
    },

    methods: {
      init() {
        const { session, paymentData } = this.get();

        this.session = session;
        paymentData.method = 'upi';
        paymentData['_[flow]'] = 'intent';
        paymentData['_[upiqr]'] = '1';

        if (session.p13n) {
          processInstrument(paymentData);
        }

        if (session.preferences.fee_bearer) {
          this.set({ view: 'fee', session, paymentData });
        } else {
          this.createPayment();
        }

        const downtimes = DowntimesStore.get();
        if (downtimes.qr && downtimes.qr.length) {
          this.set({
            down: true,
          });
        }
      },

      handleResponse({ data }) {
        const qrImage = `https://chart.googleapis.com/chart?chs=200x200&cht=qr&chl=${encodeURIComponent(
          data.qr_code_url || data.intent_url
        )}&choe=UTF-8&chld=L|0`;
        this.set({ qrImage, loading: false });
        this.session.r.emit('payment.upi.intent_success_response');
      },

      checkStatus() {
        this.session.showLoadError('Checking payment status...');
      },

      onError(data) {
        this.set({
          view: 'error',
          error: data.error.description,
          loading: false
        });
      },

      createPaymentWithFees(bearer) {
        const { paymentData } = this.get();

        paymentData.amount = bearer.amount;
        paymentData.fee = bearer.fee;

        this.createPayment();
      },

      sessionBack() {
        const {
          session
        } = this.get();

        session.switchTab('upi');
      },

      createPayment() {
        this.set({
          view: 'qr',
          loading: true
        });

        const { session, paymentData, onSuccess } = this.get();

        if (session.methodsList) {
          processInstrument(paymentData);
        }

        /**
         * TODO:
         * We should call session.submit from here
         * and let it handle fees and other eventing stuff
         */
        global.Razorpay.sendMessage({
          event: 'submit',
          data: paymentData,
        });

        session.r
          .createPayment(paymentData, {
            upiqr: true,
            optional: session.optional,
            paused: session.get().paused,
          })
          .on(
            'payment.upi.coproto_response',
            _Func.bind(this.handleResponse, this)
          )
          .on('payment.success', onSuccess)
          .on('payment.error', _Func.bind(this.onError, this));
      },
    },
  };
</script>
